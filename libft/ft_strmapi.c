#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	size_t	l;
	char	*newstr;

	if (!s)
		return (NULL);
	if (!f)
		return (NULL);
	l = ft_strlen(s);
	newstr = (char *)malloc(sizeof(char) * (l + 1));
	if (!newstr)
		return (NULL);
	i = 0;
	while (s[i])
	{
		newstr[i] = f(i, s[i]);
		i++;
	}
	newstr[i] = '\0';
	return (newstr);
}
